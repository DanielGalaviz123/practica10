public class Posicion {
    private int renglo;
    private int columna;

    public Posicion(int renglo, int columna){
        this.renglo=renglo;
        this.columna=columna;
    }


    public int getRenglon(){
        return renglo;
    }

    public int getColumna(){
        return columna;
    }
}
